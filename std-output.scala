// In Scala, all code needs to be within a method, block, or object.
object Main {
  def main(args: Array[String]): Unit = {
    val name = "Aria"
    val age = 22
    
    // add variables to strings
    println(s"Hello, ${name}! You are ${age} years old.")
    
    // semicolons are only required if 
    // you have multiple statements in the same line
    val x = 1; val y = 2
  }
}
